#include "main.h"

/**
 * mul - function multiplies two given numbers
 * @a: first number, the multiplicand
 * @b: second number, the multiplier
 * Description: prints result of multiplication (product)
 * Return: product value
 */

int mul(int a, int b)
{
	int product;

	product = a * b;
	return (product);
}
